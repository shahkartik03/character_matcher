function stringMatcher() {
  const name = document.getElementById("name").value;
  const patterObj = {};
  elements.forEach(e => {
    let len = e.symbol.length;
    if (patterObj[len]) {
      const val = patterObj[len];
      val.push(e.symbol);
      patterObj[len] = val;
    } else {
      patterObj[len] = [e.symbol];
    }
  });
  // console.log(patterObj);
  const patterArr = Object.entries(patterObj);
  // console.log(patterArr);
  const res = {};
  patterArr.forEach( p => {
    for (var i=0; i < name.length - p[0] + 1; i++) {
      console.log(name.substring(i, (i*1) +(p[0]*1)));
      p[1].forEach(t => {
        if (t === name.substring(i, (i*1) +(p[0]*1))) {
            res[i] = t;
        }
      })
  }
  });
  console.log('ress ', res);
  let selectedChar;
  var div = document.createElement('div');
  var nameArr = name.split("");
  nameArr.forEach((c, i) => {
    var span = document.createElement('span');
    span.textContent = c;
    if (!!res[i]) {
      console.log('in iff  ', res);
      span.setAttribute('class', 'highlighted');
    }
    div.appendChild(span);
  });
  // div.textContent = "Sup, y'all?";
  div.setAttribute('class', 'result');
  document.body.appendChild(div);
  var matchedChar = document.createElement('div');
  matchedChar.setAttribute('class', 'charDiv');
  var charArr = Object.entries(res);
  charArr.forEach((char, i) => {
    var span = document.createElement('span');
    span.textContent = char[1];
    span.addEventListener("click", () => {
      selectedChar = charArr[i];
      console.log('selectedChar  ', selectedChar);
    });
     matchedChar.appendChild(span);   
  });
  selectedChar = charArr[0];
  console.log('selectedChar  ', selectedChar);
  document.body.appendChild(matchedChar);
}